package Day3;

public class Q3 {
        public static void main(String[] args) {
            for (int i = 1; i <= 3; i++) {  // Loop for 1, 2, 3
                System.out.println("" + i);  // Print the number with spaces
    
                for (int j = 1; j <= 4; j++) {  // Loop to print 1 2 3 4
                    System.out.print(j + " ");
                }
                System.out.println();  // Move to next line
            }
        }
    }
    
